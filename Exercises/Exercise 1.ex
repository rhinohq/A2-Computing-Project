Printing

[ExDescription]
Welcome to Lua. In this exercise, you are going to learn to print to the console using the print() function.

In Lua, we are able to display text to our users by using a command called print. This can be used like so: 
print("Hello, User!")

In the code editor, try printing 'Hello world!' to the console.

